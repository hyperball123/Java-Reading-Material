package hit.day11;

public class VariableDemo {
	/*
	 * Broadly classifying, there are two types of variables.
	 * 1. Simple Type - byte, short,int, long, double, float etc
	 * 2. Complex Type- any class variable is a complex type
	 * 
	 * for ex:
	 * int i;//simple type
	 * VariableDemo vd;//complex type
	 * A class is a noun - Human shoiab; - complex type
	 * String shoiab;//simple type
	 * 
	 * Any variable u declare with a class name is a complex type variable
	 * 
	 * SCOPES
	 * 
	 * 1. Local scope
	 * 2. Instance scope
	 * 3. class scope
	 */
	
	/*
	 * To declare a instance variable
	 * Just declare any variable outside the methods i.e at class level, 
	 * it will be taken as instance variable.
	 * 
	 * To declare a local variable
	 * Just declare any variable inside the methods i.e. within the braces of the methods
	 * it will be taken as local variable
	 * 
	 * To declare a class variable
	 * Just declare any variable outside the methods as STATIC (using static key word)
	 * it will be taken as class variable
	 * Class variables
	 * 1. will be automatically instantiated by the JVM, no need to create an object to access
	 * 2. only one copy of the class variable will be created.and that will be shared among 
	 * all objects of that class
	 * 
	 */
}
